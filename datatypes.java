class datatype{
    public static void main (String a[]){
        float num1 = 7.5f;
        int   num2 = 6 ;
        byte  num3 = 127;
        long  num4 = 1344555l;
        boolean b  = true ;
        char c = 'k';
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(b);
        System.out.println(c);
    }
}